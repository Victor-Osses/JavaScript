// getElementsByTagName - Retorna um HTML Collection do elemento especificado por meio da tag
var titulo = document.getElementsByTagName('h1')[0]; 
console.log(titulo);

var lis = document.getElementsByTagName('li');
console.log(lis[3]);

// getElementById - Retorna o elemento com o id especificado 
var paragrafo = document.getElementById('paragrafo');
console.log(paragrafo);

// getElementByClassName - Retorna um HTML Collection do elemento especificado por meio da classe
var itensDaLista = document.getElementsByTagName('li'); 
console.log(itensDaLista);

